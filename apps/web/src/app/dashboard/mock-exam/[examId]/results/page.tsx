"use client";

import { useEffect, useState } from "react";
import { useParams, useRouter } from "next/navigation";
import Link from "next/link";
import { ResultsDashboard } from "@/components/mock-exam/results-dashboard";
import type { MockExamResult, MockExam } from "@/types/mock-exam";

export default function ExamResultsPage() {
  const params = useParams();
  const router = useRouter();
  const examId = params.examId as string;

  const [exam, setExam] = useState<MockExam | null>(null);
  const [results, setResults] = useState<MockExamResult | null>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    const loadResults = async () => {
      try {
        const response = await fetch(`/api/mock-exam/${examId}/results`);
        if (!response.ok) {
          throw new Error("Failed to load exam results");
        }
        const data = await response.json();
        setResults(data.results);
        setExam(data.exam);
        setLoading(false);
      } catch (err) {
        setError((err as Error).message);
        setLoading(false);
      }
    };

    loadResults();
  }, [examId]);

  const handleDownloadReport = () => {
    if (!exam || !results) return;

    const reportContent = generateTextReport(exam, results);
    const blob = new Blob([reportContent], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `${exam.title.replace(/[^a-z0-9]/gi, "_")}_results.txt`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  const handleRetake = () => {
    router.push("/dashboard/mock-exam/new");
  };

  const generateTextReport = (examData: MockExam, resultsData: MockExamResult): string => {
    const date = new Date().toLocaleDateString();
    const time = new Date().toLocaleTimeString();

    return `
MOCK EXAM RESULTS REPORT
========================

Exam: ${examData.title}
Date: ${date} at ${time}
Time Limit: ${examData.time_limit_minutes} minutes
Time Spent: ${resultsData.timeSpentMinutes} minutes

OVERALL SCORE
=============
Grade: ${resultsData.grade}
Percentage: ${resultsData.totalPercentage}%
Multiple Choice: ${resultsData.mcqScore}/${resultsData.mcqTotal} (${Math.round((resultsData.mcqScore / resultsData.mcqTotal) * 100)}%)
Essays: ${resultsData.essayScores.reduce((sum, s) => sum + s.totalScore, 0)}/${resultsData.essayScores.reduce((sum, s) => sum + s.maxScore, 0)} points

MULTIPLE CHOICE BREAKDOWN
=========================
Score: ${resultsData.mcqScore}/${resultsData.mcqTotal}
Strengths: ${resultsData.topicsStrengths.join(", ") || "None identified"}
Areas for Review: ${resultsData.topicsWeaknesses.join(", ") || "None identified"}

ESSAY BREAKDOWN
================
${resultsData.essayScores.map((essay, index) => `
Essay ${index + 1}:
Score: ${essay.totalScore}/${essay.maxScore} (${Math.round((essay.totalScore / essay.maxScore) * 100)}%)
Time Spent: ${essay.timeSpentMinutes} minutes
Overall Feedback: ${essay.overallFeedback}
`).join("\n")}

AI FEEDBACK
===========
Strengths:
${resultsData.feedback.strengths.map(s => `- ${s}`).join("\n")}

Areas for Improvement:
${resultsData.feedback.areasForImprovement.map(a => `- ${a}`).join("\n")}

Study Recommendations:
${resultsData.feedback.studyRecommendations.map(r => `- ${r}`).join("\n")}

Next Steps:
${resultsData.feedback.nextSteps.map(n => `- ${n}`).join("\n")}

Predicted Grade: ${resultsData.feedback.predictedGrade}

SOURCE DOCUMENTS
================
${examData.source_filenames.join("\n")}

Generated by AutoQuiz Mock Exam System
https://your-domain.com
    `.trim();
  };

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-center">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-indigo-500 mx-auto mb-4"></div>
          <p className="text-slate-600 dark:text-slate-400">Loading results...</p>
        </div>
      </div>
    );
  }

  if (error || !results || !exam) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-center max-w-md">
          <div className="text-6xl mb-4">😕</div>
          <h1 className="text-2xl font-bold text-slate-900 dark:text-slate-100 mb-2">
            Results Error
          </h1>
          <p className="text-slate-600 dark:text-slate-400 mb-6">
            {error || "Results not found"}
          </p>
          <button
            onClick={() => router.push("/dashboard/mock-exam")}
            className="px-4 py-2 bg-indigo-500 text-white rounded-lg hover:bg-indigo-600"
          >
            Back to Mock Exams
          </button>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-slate-50 dark:bg-slate-900 py-8">
      <div className="max-w-6xl mx-auto px-4">
        <ResultsDashboard
          results={results}
          examTitle={exam.title}
          sourceDocuments={exam.source_filenames}
          timeLimitMinutes={exam.time_limit_minutes}
          onRetake={handleRetake}
          onDownloadReport={handleDownloadReport}
        />

        {/* Back to Dashboard */}
        <div className="text-center mt-8">
          <Link
            href="/dashboard/mock-exam"
            className="text-sm text-slate-500 hover:text-slate-700 dark:text-slate-400 dark:hover:text-slate-300"
          >
            ← Back to Mock Exam Center
          </Link>
        </div>
      </div>
    </div>
  );
}